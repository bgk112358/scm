void hex_dump_log(const char *title, const unsigned char *c, unsigned int size) 
{
    printf("%s: len = %d\n", title, size);
    int i;
    for(i = 0; i < size; i++) 
    {
        printf("0x%02x,",c[i]);  
        if((i>0 ) &&(i%16 ==0))
        {
            printf("\n");
        }
    }
    printf("\n");
}

